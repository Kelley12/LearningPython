# dictionary Functions covered in Chapter 5

dictionary = {'Blake': 'Nov 23', 'Alyssa': 'Mar 23', 'Carol': 'Mar 4'}

print('The dictionary in question:')
print(dictionary)
print()
print('keys() function returns')
print(dictionary.keys())
print()
print('values() function returns')
print(dictionary.values())
print()
print('items() function returns')
print(dictionary.items())
